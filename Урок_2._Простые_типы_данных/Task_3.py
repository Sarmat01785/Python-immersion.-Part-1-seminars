"""
Задача 3

Напишите программу, которая получает целое число и возвращает его двоичное, восьмеричное,строковое представление.
Функции bin и oct используйте для проверки своего результата,а не для решения.

Дополнительно:
1. Попробуйте избежать дублирования кода в преобразованиях к разным системам счисления.
2. Избегайте "магических чисел"
3. Добавьте аннотацию типов где это возможно
"""


def int_to_base(n: int, base: int) -> str:
    """Преобразует целое число в строковое представление в заданной системе счисления."""
    if n == 0:
        return "0"

    digits = "0123456789ABCDEF"
    res = ""
    while n > 0:
        res = digits[n % base] + res
        n //= base

    return res


def convert_number(num: int) -> tuple[str, str, str]:
    """
    Преобразует число num в двоичное, восьмеричное и строковое представление.
    :param num: Целое число, которое нужно преобразовать.
    :return: Кортеж с тремя строками: двоичным, восьмеричным и строковым представлением числа.
    """
    binary = int_to_base(num, 2)
    octal = int_to_base(num, 8)
    string = str(num)
    return binary, octal, string


# Пример использования
num = int(input("Введите целое число: "))
binary, octal, string = convert_number(num)
print("Двоичное представление:", binary)
print("Восьмеричное представление:", octal)
print("Строковое представление:", string)

# Проверка результата с использованием встроенных функций bin и oct
print("Проверка:")
print("Двоичное представление (bin):", bin(num)[2:])
print("Восьмеричное представление (oct):", oct(num)[2:])
