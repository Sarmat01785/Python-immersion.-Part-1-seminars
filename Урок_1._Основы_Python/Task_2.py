'''
Задача 2

Пользователь вводит число от 1 до 999.
Используя операции с числами сообщите что введено: однозначное число, двузначное чисоло или трехзначное число.
Для однозначного числа верните её квадрат, например 5 = 25
Для двухзначного числа произведение цифр, например 30 = 3
Для техзначного чила его зеркальное отображение, например 520 = 25
Если число не издиапазона, запросите новое число.
В коде должен быть один input и один print.
'''

# Вариант 1

# Считываем число от пользователя
number = int(input("Введите число от 1 до 999: "))

# Проверяем, что число находится в диапазоне от 1 до 999
while number < 1 or number > 999:
    print("Число должно быть от 1 до 999")
    number = int(input("Введите число от 1 до 999: "))

# Определяем количество цифр в числе и выполняем соответствующую операцию
if number < 10:
    # Однозначное число
    print(number   *   number)  # Выводим квадрат числа
elif number < 100:
    # Двузначное число
    first_digit = number // 10  # Получаем первую цифру числа
    second_digit = number % 10  # Получаем вторую цифру числа
    print(first_digit   *   second_digit)  # Выводим произведение цифр
else:
    # Трехзначное число
    reversed_number = str(number)[::-1]  # Зеркальное отображение числа
    print(int(reversed_number))  # Выводим зеркальное отображение числа



# Вариант 2

# Запрос числа от пользователя
number = int(input("Введите число от 1 до 999: "))

# Переменная для хранения результата
result = ""

# Проверка числа и выполнение соответствующих операций
if 1 <= number <= 9:
    result = f"Однозначное число, его квадрат: {number ** 2}"
elif 10 <= number <= 99:
    result = f"Двузначное число, произведение его цифр: {(number // 10) * (number % 10)}"
elif 100 <= number <= 999:
    result = f"Трехзначное число, его зеркальное отображение: {int(str(number)[::-1])}"
else:
    result = "Число не входит в заданный диапазон от 1 до 999."

# Вывод результата
print(result)